package ViewController;

class Theme {

    String main;
    String main_dark;
    String primary;
    String primary_dark;
    String secondary;
    String secondary_dark;
    String tertiary;
    String tertiary_dark;
    String extreme_color;

    Theme(String main, String main_dark , String primary, String primary_dark, String secondary, String secondary_dark, String tertiary, String tertiary_dark, String extreme_color){

        this.main = main;
        this.main_dark = main_dark;
        this.primary = primary;
        this.primary_dark = primary_dark;
        this.secondary = secondary;
        this.secondary_dark = secondary_dark;
        this.tertiary = tertiary;
        this.tertiary_dark = tertiary_dark;
        this.extreme_color = extreme_color;
    }
}
